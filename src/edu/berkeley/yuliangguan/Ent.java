package edu.berkeley.yuliangguan;

public class Ent {
    public final String desti;
    public final String eta;

    Ent(String desti, String eta) {
        this.desti = desti;
        this.eta = eta;

    }
    
    public String getDesti() {
    	return this.desti;
    }
    
    public String getETA() {
    	return this.eta;
    }
    
}